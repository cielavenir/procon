require 'zlib'
require 'json'
M=10**9+7
n=gets.to_s.to_i
if n>=M
	p 0
else
	bse=100000
	d,n=n.divmod bse
	r=JSON.parse(Zlib.inflate(DATA.read.unpack('m')[0]))[d]
	n.times{|i|
		r=r*(d*bse+i+1)%M
	}
	p r
end
