require 'digest/md5'
def parsefasta(f)
	ret=Hash.new{|h,k|h[k]=[]}
	head=f.gets.chomp[1..-1]
	data=[]
	while line=f.gets
		if line.start_with?('>')
			ret[head.to_i]=data
			head=line.chomp[1..-1]
			data=[]
		else
			data<<line.chomp
		end
	end
	ret[head.to_i]=data
	ret
end
data=parsefasta(DATA)
